#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	j;

	j = 0;
	while ((s1[j] != '\0' || s2[j] != '\0') && j < n)
	{
		if (s1[j] > s2[j])
		{
			return (1);
		}
		else if (s1[j] < s2[j])
		{
			return (-1);
		}
		j++;
	}
	return (0);
}
